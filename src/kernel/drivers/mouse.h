#ifndef MOUSE_H
#define MOUSE_H

#include "../kklibc/ctypes.h"
#include "screen.h"

/* ──────────────────────────────────────────────────────────────────────────
 * Порты PS/2 контроллера
 * ────────────────────────────────────────────────────────────────────────── */
#define MOUSE_DATA_PORT    0x60   /* Порт данных PS/2                        */
#define MOUSE_CMD_PORT     0x64   /* Порт команд / статуса PS/2              */

/* ──────────────────────────────────────────────────────────────────────────
 * Команды контроллера PS/2 (порт 0x64)
 * ────────────────────────────────────────────────────────────────────────── */
#define MOUSE_CMD_ENABLE_AUX    0xA8   /* Включить вспомогательное устройство */
#define MOUSE_CMD_WRITE_AUX     0xD4   /* Следующий байт → мышь               */
#define MOUSE_CMD_GET_COMPAQ    0x20   /* Читать байт конфигурации Compaq     */
#define MOUSE_CMD_SET_COMPAQ    0x60   /* Записать байт конфигурации Compaq   */

/* ──────────────────────────────────────────────────────────────────────────
 * Команды мыши (отправляются через порт 0x60 после MOUSE_CMD_WRITE_AUX)
 * ────────────────────────────────────────────────────────────────────────── */
#define MOUSE_SET_DEFAULTS      0xF6   /* Восстановить параметры по умолчанию */
#define MOUSE_ENABLE_PACKETS    0xF4   /* Включить передачу пакетов           */
#define MOUSE_SET_SAMPLE_RATE   0xF3   /* Задать частоту дискретизации        */
#define MOUSE_GET_DEVICE_ID     0xF2   /* Запросить идентификатор устройства  */
#define MOUSE_ACK               0xFA   /* Подтверждение от мыши               */

/* ──────────────────────────────────────────────────────────────────────────
 * Биты байта состояния (первый байт пакета)
 * ────────────────────────────────────────────────────────────────────────── */
#define MOUSE_BTN_LEFT          0x01   /* Бит левой кнопки                    */
#define MOUSE_BTN_RIGHT         0x02   /* Бит правой кнопки                   */
#define MOUSE_BTN_MIDDLE        0x04   /* Бит средней кнопки                  */
#define MOUSE_ALWAYS_ONE        0x08   /* Всегда 1 (валидация пакета)         */
#define MOUSE_X_SIGN            0x10   /* Знаковый бит дельты X               */
#define MOUSE_Y_SIGN            0x20   /* Знаковый бит дельты Y               */
#define MOUSE_X_OVERFLOW        0x40   /* Переполнение по X                   */
#define MOUSE_Y_OVERFLOW        0x80   /* Переполнение по Y                   */

/* ──────────────────────────────────────────────────────────────────────────
 * Границы курсора (текстовый режим VGA 80x25)
 * ────────────────────────────────────────────────────────────────────────── */
#define MOUSE_MIN_X   0
#define MOUSE_MAX_X   (MAX_COLS - 1)    /* 79  */
#define MOUSE_MIN_Y   0
#define MOUSE_MAX_Y   (MAX_ROWS - 1)    /* 24  */

/* Символ-курсор мыши в текстовом режиме (блок █)                           */
#define MOUSE_CURSOR_CHAR   0xDB
/* Цвет курсора: яркий белый на чёрном                                      */
#define MOUSE_CURSOR_COLOR  0x0F

/* ──────────────────────────────────────────────────────────────────────────
 * Структура состояния мыши
 * ────────────────────────────────────────────────────────────────────────── */
typedef struct {
    int x;              /* Текущая колонка курсора (0..79)                   */
    int y;              /* Текущая строка курсора  (0..24)                   */
    int dx;             /* Дельта X последнего движения                      */
    int dy;             /* Дельта Y последнего движения                      */
    u8  buttons;        /* Битовая маска нажатых кнопок                      */
    u8  prev_char;      /* Символ, сохранённый под курсором                  */
    u8  prev_attr;      /* Атрибут, сохранённый под курсором                 */
} mouse_state_t;

/* ──────────────────────────────────────────────────────────────────────────
 * Публичное API
 * ────────────────────────────────────────────────────────────────────────── */

/**
 * @brief Инициализация драйвера мыши PS/2
 *
 * Включает вспомогательный порт PS/2, разрешает прерывание IRQ12
 * и регистрирует обработчик в таблице IDT.
 */
void mouse_init(void);

/**
 * @brief Получить текущую позицию X курсора
 * @return Колонка (0..79)
 */
int mouse_get_x(void);

/**
 * @brief Получить текущую позицию Y курсора
 * @return Строка (0..24)
 */
int mouse_get_y(void);

/**
 * @brief Проверить состояние левой кнопки
 * @return 1 если нажата, 0 иначе
 */
int mouse_button_left(void);

/**
 * @brief Проверить состояние правой кнопки
 * @return 1 если нажата, 0 иначе
 */
int mouse_button_right(void);

/**
 * @brief Проверить состояние средней кнопки
 * @return 1 если нажата, 0 иначе
 */
int mouse_button_middle(void);

/**
 * @brief Получить полное состояние мыши (только чтение)
 * @return Указатель на структуру mouse_state_t
 */
const mouse_state_t *mouse_get_state(void);

/**
 * @brief Принудительно перерисовать курсор на экране
 *
 * Используется после очистки экрана или смены режима вывода.
 */
void mouse_redraw_cursor(void);

/**
 * @brief Скрыть курсор мыши (восстановить символ под ним)
 */
void mouse_hide_cursor(void);

#endif /* MOUSE_H */
